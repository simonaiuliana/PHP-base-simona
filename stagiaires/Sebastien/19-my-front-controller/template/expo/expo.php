      <h1 class="titre">Les souvenir de l'informatique</h1>
      <br />
      <section class="text">
        Arrivés au musée
        <a href="https://www.nam-ip.be/en/front-page-english/" target="_blank"
          >Nam-IP </a
        >, nous avons joué à quelques jeux de logique en groupe avant de passer
        aux choses sérieuses : la visite du musée. Mais d'abord, une pause
        déjeuner bien méritée ! En revenant des toilettes, j'aperçois l'entrée
        du musée. Ma curiosité me pousse à aller voir rapidement comment est le
        musée . <br /><br />
        La première chose que j'ai remarquée était
        <span class="bgspan" id="manneq">le mannequin</span> à la machine à
        écrire. Puis, soudainement, j'ai vu une ancienne console allumée. La
        petite <span class="bgspan" id="manette">manette</span> que j'avais dans
        les mains a ravivé tant de souvenirs d'enfance que je me suis senti
        obligé de la tester. Dans le silence de la salle vide, je me suis envolé
        dans mes souvenirs les plus lointains, retournant à l'époque de mon
        enfance des années 90. <br /><br />

        En voyant les jeux auxquels je jouais étant enfant, Je me suis revu le
        jour de Noël chez ma grand-mère, où j'avais reçu ma toute première
        <span class="bgspan" id="noel">Sega</span> avec le jeu
        <span class="bgspan" id="asterix">Astérix</span>. J'ai pu le réessayer
        sur cette fameuse console qui regroupait toutes les anciennes consoles
        de mon enfance, comme la
        <span class="bgspan" id="nes">Nintendo</span> ou la
        <span class="bgspan" id="boy">Game Boy</span>. En parcourant les jeux,
        je suis tombé sur des jeux oubliés tels que Dragon Ball Budokai,
        <span class="bgspan" id="kid">Alex Kidd</span> ou Metal Slug. Cela m'a
        rappelé tellement de souvenirs d'enfance, comme les samedis après-midi
        passés à jouer toute la journée avec<span class="bgspan" id="retro">
          mon meilleur ami</span
        >, ou les week-ends passés chez
        <span class="bgspan" id="cousin">mon cousin et ma cousine</span> à jouer
        à la console toute la nuit. <br /><br />

        De retour de mon voyage dans le temps, tout le monde a commencé à
        arriver pour faire la visite du musée. Pendant cette petite balade en
        solitaire, les sauts dans le passé ne cessaient pas. Je suis tombé, par
        exemple, sur
        <span class="bgspan" id="phone">un vieux téléphone</span> que je me
        souvenais avoir utilisé chez ma grand-mère quand j'étais petit, ainsi
        que sur cet ancien PC qui m'a rappelé mon tout
        <span class="bgspan" id="pc">premier ordinateur.</span> <br />
        C'est là que j'ai littéralement découvert le WEB pour la première fois :
        des sites comme<span class="bgspan" id="you">
          YouTube, Dailymotion </span
        >ou les soirées
        <span class="bgspan" id="msn">MSN</span>
        <br />Les sites étaient incroyablement
        <span class="bgspan" id="lent">lents</span>, de même que la connexion
        Internet.
        <span class="bgspan" id="emul">Télécharger un film</span> prenait 5
        jours ! Vingt ans plus tard, tout a évolué de manière spectaculaire,
        avec des sites rapides et une connexion Internet
        <span class="bgspan" id="fiber">ultra-rapide</span>. Les ordinateurs
        portables sont passés de <span class="bgspan" id="mac">ceci</span> à
        <span class="bgspan" id="zen">cela</span>, les jeux sont passés de
        <span class="bgspan" id="bit">8 bits</span> à quelque chose
        <span class="bgspan" id="ps5">4k ultra-réaliste</span>. Où s'arrêtera le
        progrès ?
        <span class="bgspan" id="futur"
          >Comment sera l'informatique dans 20 ans</span
        >
        ? L'avenir nous le dira !"
