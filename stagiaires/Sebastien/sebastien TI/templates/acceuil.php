        <h3>Dublin est la plus grande ville de l'île d'Irlande et de l'État d'Irlande, dont elle est la capitale (Belfast étant la capitale de l'Irlande du Nord). La ville est située à proximité du point central de la côte orientale de l'île et au centre du comté de Dublin. Dublin est la plus grande ville d'Irlande en importance et en nombre d’habitants depuis le haut Moyen Âge. Elle est aujourd’hui classée à la soixante-sixième place dans l’index des places financières mondiales et a un des plus forts taux de développement parmi les capitales européennes. Dublin est le centre historique, politique, artistique, culturel, économique et industriel de l’Irlande.
    
    La population de la commune de Dublin est de 554 554 habitants au recensement de 2016. Au même recensement, le comté de Dublin — divisé en quatre zones administratives : ville de Dublin, Dublin Sud, Fingal et Dún Laoghaire-Rathdown — compte 1 270 603 habitants tandis que la région du Grand Dublin abrite 1 804 156 habitants.
    
    Le nom de « Dublin » est généralement considéré comme provenant du gaélique originel Dubh Linn (« l'étang noir ») qui signifie maintenant « baie de la fumée », le nom d'un bassin d'un affluent de la Liffey, près duquel s'est érigée la première place forte des Vikings irlandais, ou Gall Gàidheal. Des doutes existent cependant à ce propos. En effet, la première référence à l'existence de la ville se trouve dans les écrits de Ptolémée aux environs de 140. Elle est alors désignée sous le nom de Eblana. La proximité de ce nom avec le nom actuel (b, l et n en commun) fait donc douter sur le lien entre Dublin et Dubh Linn, mais on ne sait pas si ces deux origines sont liées. Le nom Dubh Linn se retrouve également en islandais : djúp lind (mare profonde), en gallois : Dulyn (du noir, et llyn lac, étang) et en breton : Dulenn (du noir, et lenn étang, mare, lac).
    
    Le nom gaélique contemporain Baile Átha Cliath (« La ville du gué des haies de roseaux ») fait référence au hameau qui se trouvait près du site de fondation de Dublin.</h3>
